const lines = rawData.split('\n');
const products = [];
lines.forEach(line => {
  const parts = line.trim().split(/\s+/);
  if (parts.length < 5) return;
  const nameParts = parts.slice(1, -4);
  const name = nameParts.join(' ');
  const price = parts[parts.length - 4];
  if (!name || name === "Ad" || !price || isNaN(parseFloat(price.replace(',', '.')))) return;
  products.push({
    id: parts[0] || Math.random().toString(36).substr(2, 9),
    name: name,
    category: getCategory(name),
    price: price,
    image: '',
    description: ''
  });
});

